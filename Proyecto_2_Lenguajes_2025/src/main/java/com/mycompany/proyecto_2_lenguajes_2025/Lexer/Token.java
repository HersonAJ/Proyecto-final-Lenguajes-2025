package com.mycompany.proyecto_2_lenguajes_2025.Lexer;

/**
 *
 * @author herson
 */
public class Token {
    
    private String tipo;
    private String valor;
    private int linea;
    private int columna;
    
    public Token(String tipo, String valor, int linea, int columna) {
        this.tipo = tipo;
        this.valor = valor;
        this.linea = linea;
        this.columna = columna;
    }

    public String getTipo() {
        return tipo;
    }

    public String getValor() {
        return valor;
    }

    public int getLinea() {
        return linea;
    }

    public int getColumna() {
        return columna;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public void setValor(String valor) {
        this.valor = valor;
    }

    public void setLinea(int linea) {
        this.linea = linea;
    }

    public void setColumna(int columna) {
        this.columna = columna;
    }
    
    @Override
    public String toString() {
        return "Token{" +
                "tipo ='" + tipo + '\'' +
                ", valor ='" + valor + '\'' +
                ", linea =" + linea +
                ", columna =" + columna +
                '}';
    }
}
